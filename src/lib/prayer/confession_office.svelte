<Confession />
<Rubric>The Priest alone stands and says</Rubric>
<TextBlock>
	Almighty God, the Father of our Lord Jesus Christ, desires not the death of sinners, but that they
	may turn from their wickedness and live. He has empowered and commanded his ministers to pronounce
	to his people, being penitent, the absolution and remission of their sins. He pardons and absolves
	all who truly repent and genuinely believe his holy Gospel. For this reason, we beseech him to
	grant us true repentance and his Holy Spirit, that our present deeds may please him, the rest of
	our lives may be pure and holy, and that at the last we may come to his eternal joy; through Jesus
	Christ our Lord. __Amen.__
</TextBlock>
<Rubric>or this</Rubric>
<TextBlock>
	The Almighty and merciful Lord grant you absolution and remission of all your sins, true
	repentance, amendment of life, and the grace and consolation of his Holy Spirit. __Amen.__
</TextBlock>
