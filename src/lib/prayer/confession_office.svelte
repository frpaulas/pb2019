<Rubric>The Officiant says to the People</Rubric>
<TextBlock>
	Dearly beloved, the Scriptures teach us to acknowledge our many sins and offenses, not concealing
	them from our heavenly Father, but confessing them with humble and obedient hearts that we may
	obtain forgiveness by his infinite goodness and mercy. We ought at all times humbly to acknowledge
	our sins before Almighty God, but especially when we come together in his presence to give thanks
	for the great benefits we have received at his hands, to declare his most worthy praise, to hear
	his holy Word, and to ask, for ourselves and on behalf of others, those things which are necessary
	for our life and our salvation. Therefore, draw near with me to the throne of heavenly grace.
