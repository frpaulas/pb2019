<SectionTitle>7</SectionTitle>
<SectionTitle>cantate domino</SectionTitle>
<SectionTitle fancy>Sing unto the Lord</SectionTitle>
<Rubric>Especially suitable for use in Easter and at any time outside penitential seasons</Rubric>

<Line bold>O sing unto the Lord a new song, *</Line>
<Line indent bold>for he has done marvelous things.</Line>
<Line bold>With his own right hand, and with his holy arm, *</Line>
<Line indent bold>he has won for himself the victory.</Line>
<Line bold>The Lord declared his salvation; *</Line>
<Line indent bold>his righteousness he has openly shown in the sight of the nations.</Line>
<Line bold>He has remembered his mercy and truth toward the house of Israel,*</Line>
<Line indent bold>and all the ends of the world have seen the salvation of our God.</Line>
<Line bold>Show yourselves joyful unto the Lord, all you lands; *</Line>
<Line indent bold>sing, rejoice, and give thanks.</Line>
<Line bold>Praise the Lord with the harp; *</Line>
<Line indent bold>sing with the harp a psalm of thanksgiving.</Line>
<Line bold>With trumpets also and horns, *</Line>
<Line indent bold>O show yourselves joyful before the Lord, the King.</Line>
<Line bold>Let the sea make a noise, and all that is in it, *</Line>
<Line indent bold>the round world, and those that dwell therein.</Line>
<Line bold>Let the rivers clap their hands, and let the hills be joyful</Line>
<Line bold>together before the Lord, *</Line>
<Line indent bold>for he has come to judge the earth.</Line>
<Line bold>With righteousness shall he judge the world *</Line>
<Line indent bold>and the peoples with equity.</Line>
<Line bold>Glory be to the Father, and to the Son, and to the Holy Spirit; *</Line>
<Line indent bold>as it was in the beginning, is now, and ever shall be,</Line>
<Line indent bold ref="psalm 98">world without end. Amen.</Line>
