<Line bold>I will sing to the Lord, for he is lofty and uplifted; *</Line>
<Line indent bold>the horse and its rider has he hurled into the sea.</Line>
<Line bold>The Lord is my strength and my refuge; *</Line>
<Line indent bold>the Lord has become my Savior.</Line>
<Line bold>This is my God and I will praise him, *</Line>
<Line indent bold>the God of my people and I will exalt him.</Line>
<Line bold>The Lord is a mighty warrior; *</Line>
<Line indent bold>The Lord is his Name.</Line>
<Line bold>The chariots of Pharaoh and his army has he hurled into the sea; *</Line>
<Line indent bold>the finest of those who bear armor</Line>
<Line indent bold>have been drowned in the Red Sea.</Line>
<Line bold>The fathomless deep has overwhelmed them; *</Line>
<Line indent bold>they sank into the depths like a stone.</Line>
<Line bold>Your right hand, O Lord, is glorious in might; *</Line>
<Line indent bold>your right hand, O Lord, has overthrown the enemy.</Line>
<Line bold>Who can be compared with you, O Lord, among the gods? *</Line>
<Line indent bold>Who is like you, glorious in holiness,</Line>
<Line indent bold>awesome in renown, and worker of wonders?</Line>
<Line bold>You stretched forth your right hand; *</Line>
<Line indent bold>the earth swallowed them up.</Line>
<Line bold>With your constant love you led the people you redeemed; *</Line>
<Line indent bold>you brought them in safety to your holy dwelling.</Line>
<Line bold>You will bring them in and plant them *</Line>
<Line indent bold>on the mount of your possession,</Line>
<Line bold>The resting-place you have made for yourself, O Lord, *</Line>
<Line indent bold>the sanctuary, O Lord, that your hand has established.</Line>
<Line bold>The Lord shall reign *</Line>
<Line indent bold>for ever and for ever.</Line>
<Line bold>Glory be to the Father, and to the Son, and to the Holy Spirit; *</Line>
<Line indent bold>as it was in the beginning, is now, and ever shall be,</Line>
<Line indent bold ref="exodus 15:1-6, 11-13, 17-18">world without end. Amen.</Line>
