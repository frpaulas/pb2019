<SectionTitle fancy>A Song of Penitence</SectionTitle>
<Rubric>Especially suitable for use during Lent</Rubric>

<Line bold>O Lord and Ruler of the hosts of heaven, *</Line>
<Line indent bold>God of Abraham, Isaac, and Jacob,</Line>
<Line indent bold>and of all their righteous offspring:</Line>
<Line bold>You made the heavens and the earth, *</Line>
<Line indent bold>with all their vast array.</Line>
<Line bold>All things quake with fear at your presence; *</Line>
<Line indent bold>they tremble because of your power.</Line>
<Line bold>But your merciful promise is beyond all measure; *</Line>
<Line indent bold>it surpasses all that our minds can fathom.</Line>
<Line bold>O Lord, you are full of compassion, *</Line>
<Line indent bold>long-suffering, and abounding in mercy.</Line>
<Line bold>You hold back your hand; *</Line>
<Line indent bold>you do not punish as we deserve.</Line>
<Line bold>In your great goodness, Lord,</Line>
<Line bold>you have promised forgiveness to sinners, *</Line>
<Line indent bold>that they may repent of their sin and be saved.</Line>
<Line bold>And now, O Lord, I bend the knee of my heart, *</Line>
<Line indent bold>and make my appeal, sure of your gracious goodness.</Line>
<Line bold>I have sinned, O Lord, I have sinned, *</Line>
<Line indent bold>and I know my wickedness only too well.</Line>
<Line bold>Therefore I make this prayer to you: *</Line>
<Line indent bold>Forgive me, Lord, forgive me.</Line>
<Line bold>Do not let me perish in my sin, *</Line>
<Line indent bold>nor condemn me to the depths of the earth.</Line>
<Line bold>For you, O Lord, are the God of those who repent, *</Line>
<Line indent bold>and in me you will show forth your goodness.</Line>
<Line bold>Unworthy as I am, you will save me,</Line>
<Line bold>in accordance with your great mercy, *</Line>
<Line indent bold>and I will praise you without ceasing all the days of my life.</Line>
<Line bold>For all the powers of heaven sing your praises, *</Line>
<Line indent bold ref="prayer of manasseh, 1-2, 4, 6-7, 11-15"
	>and yours is the glory to ages of ages. Amen.</Line
