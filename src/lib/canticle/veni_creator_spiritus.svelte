<Line>Who dost thy sevenfold gifts impart.</Line>
<br />
<Line>Thy blessed unction from above</Line>
<Line>Is comfort, life, and fire of love.</Line>
<Line>Enable with perpetual light</Line>
<Line>The dullness of our blinded sight.</Line>
<br />
<Line>Anoint and cheer our soiled face</Line>
<Line>With the abundance of thy grace.</Line>
<Line>Keep far our foes, give peace at home;</Line>
<Line>Where thou art guide, no ill can come.</Line>
<br />
<Line>Teach us to know the Father, Son,</Line>
<Line>And thee, of both, to be but One;</Line>
<Line>That, through the ages all along,</Line>
<Line>This may be our endless song</Line>
<br />
<Line>Praise to thy eternal merit,</Line>
<Line>Father, Son, and Holy Spirit.</Line>
