<SectionTitle fancy>O Come</SectionTitle>
<Line bold>O come, let us sing unto the Lord; *</Line>
<Line indent bold>let us heartily rejoice in the strength of our salvation.</Line>
<Line bold>Let us come before his presence with thanksgiving *</Line>
<Line indent bold>and show ourselves glad in him with psalms.</Line>
<Line bold>For the Lord is a great God *</Line>
<Line indent bold>and a great King above all gods.</Line>
<Line indent bold>n his hand are all the depths of the earth, *</Line>
<Line indent bold>and the heights of the hills are his also.</Line>
<Line bold>The sea is his, for he made it, *</Line>
<Line indent bold>and his hands prepared the dry land.</Line>
<Line bold>O come, let us worship and fall down, *</Line>
<Line indent bold>and kneel before the Lord our Maker.</Line>
<Line bold>For he is our God, *</Line>
<Line indent bold>and we are the people of his pasture,</Line>
<Line indent bold>and the sheep of his hand.</Line>

<Rubric>The following verses may be omitted, except in Lent.</Rubric>
<Line bold>Today, if you will hear his voice, harden not your hearts *</Line>
<Line indent bold>as in the provocation, and as in the day of temptation in</Line>
<Line indent bold>the wilderness,</Line>
<Line bold>When your fathers tested me, *</Line>
<Line indent bold>and put me to the proof, though they had seen my works.</Line>
<Line bold>Forty years long was I grieved with this generation and said, *</Line>
<Line indent bold>"It is a people that err in their hearts,</Line>
<Line indent bold>for they have not known my ways,"</Line>
<Line bold>Of whom I swore in my wrath *</Line>
<Line indent bold>that they should not enter into my rest.</Line>
