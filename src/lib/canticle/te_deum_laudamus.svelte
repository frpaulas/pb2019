<SectionTitle fancy>We Praise You, O God</SectionTitle>
<Line bold>We praise you, O God; we acclaim you as Lord; *</Line>
<Line indent bold>all creation worships you, the Father everlasting.</Line>
<Line bold>To you all angels, all the powers of heaven, *</Line>
<Line indent bold>the cherubim and seraphim, sing in endless praise:</Line>
<Line bold>Holy, Holy, Holy, Lord God of power and might, *</Line>
<Line indent bold>heaven and earth are full of your glory.</Line>
<Line bold>The glorious company of apostles praise you. *</Line>
<Line indent bold>The noble fellowship of prophets praise you.</Line>
<Line bold>The white-robed army of martyrs praise you. *</Line>
<Line indent bold>Throughout the world the holy Church acclaims you:</Line>
<Line bold>Father, of majesty unbounded,</Line>
<Line bold>your true and only Son, worthy of all praise, *</Line>
<Line indent bold>and the Holy Spirit, advocate and guide.</Line>
<Line bold>You, Christ, are the king of glory, *</Line>
<Line indent bold>the eternal Son of the Father.</Line>
<Line bold>When you took our flesh to set us free *</Line>
<Line indent bold>you humbly chose the Virgin's womb.</Line>
<Line bold>You overcame the sting of death *</Line>
<Line indent bold>and opened the kingdom of heaven to all believers.</Line>
<Line bold>You are seated at God's right hand in glory. *</Line>
<Line indent bold>We believe that you will come to be our judge.</Line>
<Line bold>Come then, Lord, and help your people, *</Line>
<Line indent bold>bought with the price of your own blood,</Line>
<Line bold>and bring us with your saints *</Line>
<Line indent bold>to glory everlasting.</Line>
<Rubric>The following verses may be omitted</Rubric>
<Line bold>Save your people, Lord, and bless your inheritance; *</Line>
<Line indent bold>govern and uphold them now and always.</Line>
<Line bold>Day by day we bless you; *</Line>
<Line indent bold>we praise your Name for ever.</Line>
<Line bold>Keep us today, Lord, from all sin; *</Line>
<Line indent bold>have mercy on us, Lord, have mercy.</Line>
<Line bold>Lord, show us your love and mercy, *</Line>
<Line indent bold>for we have put our trust in you.</Line>
<Line bold>In you, Lord, is our hope; *</Line>
<Line indent bold>let us never be put to shame.</Line>
