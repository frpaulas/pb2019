<TextBlock>subject to evil and death, you, in your mercy, sent your only Son
Jesus Christ into the world for our salvation. By the Holy Spirit
and the Virgin Mary he became flesh and dwelt among us. In
obedience to your will, he stretched out his arms upon the Cross
and offered himself once for all, that by his suffering and death
we might be saved. By his resurrection he broke the bonds of
death, trampling Hell and Satan under his feet. As our great
high priest, he ascended to your right hand in glory, that we
might come with confidence before the throne of grace.</TextBlock>
<Rubric>At the following words concerning the bread, the Celebrant is to hold it, or lay
a hand upon it, and here* may break the bread; and at the words concerning the
cup, to hold or place a hand upon the cup and any other vessel containing the wine
to be consecrated.</Rubric>
<TextBlock>On the night that he was betrayed, our Lord Jesus Christ took
bread; and when he had given thanks, he broke it,* and gave
it to his disciples, saying, “Take, eat; this is my Body, which is
given for you: Do this in remembrance of me.”</TextBlock>
<TextBlock>Likewise, after supper, Jesus took the cup, and when he had
given thanks, he gave it to them, saying, “Drink this, all of you;
for this is my Blood of the New Covenant, which is shed for
you, and for many, for the forgiveness of sins: Whenever you
drink it, do this in remembrance of me.”</TextBlock>
<Line>Therefore we proclaim the mystery of faith:</Line>
