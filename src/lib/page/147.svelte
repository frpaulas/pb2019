Communion of the Body and Blood of our Savior Jesus Christ,
you must consider how Saint Paul, in his First Letter to the
Corinthians, exhorts us all diligently to examine ourselves before
we presume to eat of that Bread, and drink of that Cup. For as
the benefit is great, if we receive that holy Sacrament with a
truly penitent heart and lively faith, spiritually eating the Flesh
of Christ and drinking his Blood, so that we might be made
one with Christ and he with us; so also is the danger great, if
we receive these gifts unworthily. For then we become guilty of
profaning the Body and Blood of Christ our Savior, and we eat
and drink to our own condemnation."
