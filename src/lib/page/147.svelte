First, examine your life by the rule of God’s commandments.
Wherever you have offended, either by thought, word, or deed,
confess your sins to Almighty God, with the full intention to
amend your life. Be ready to make restitution for all injuries
and wrongs you have done to others; and also be ready to
forgive others who have offended you: for otherwise, if you
unworthily receive Holy Communion, you will increase your
own condemnation. Therefore, repent of your sins, or else do not
come to God’s Holy Table."
