<TextBlock>In the Prayer of Humble Access, “Apart from your grace,” may
be inserted at the beginning of sentence: “We are not worthy so
much as to gather up the crumbs under your table; but you are
the same Lord whose character is always to have mercy.”</TextBlock>
<TextBlock>The words used when the Bread and Cup are given to the
    communicants may be taken from either Eucharistic Text.</TextBlock>
<TextBlock>When the Priest is assisted by a Deacon or another Priest, it is
customary for the presiding Priest to administer the consecrated
Bread. The administration of consecrated Bread and Wine by
Priests, Deacons and authorized laity shall be determined by the
Ordinary.</TextBlock>
<TextBlock>If the consecrated Bread or Wine does not suffice for the
number of communicants, the Celebrant returns to the Holy
Table and consecrates more of either or both saying,</TextBlock>
<TextBlock indent>“Hear us, O heavenly Father, and with your Word and Holy Spirit bless and sanctify this Bread [Wine]
that it, also, may be the Sacrament of the precious Body [Blood] of your Son Jesus Christ our Lord, who
took Bread [the Cup] and said, “This is my Body [Blood].”</TextBlock>
<TextBlock>If any consecrated Bread or Wine remains after the
Communion, it may be set aside in a safe place for future
reception. Apart from that which is to be set aside, the Priest
or Deacon, and other communicants, reverently consume the
remaining consecrated Bread, either after the Ministration of
Communion or after the Dismissal. The consecrated Wine shall
likewise be consumed, except as authorized and directed by the
Bishop.</TextBlock>
<TextBlock>In the Eastertide Dismissal, “Alleluia, alleluia” is customarily
added to the beginning of the Deacon’s verse when spoken, or to
the end of the Deacon’s verse when sung.</TextBlock>
