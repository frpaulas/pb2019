<TextBlock>Therefore, O Lord and heavenly Father, according to the
institution of your dearly beloved Son our Savior Jesus Christ,
we your humble servants celebrate and make here before your
divine Majesty, with these holy gifts, the memorial your Son
commanded us to make; remembering his blessed passion and
precious death, his mighty resurrection and glorious ascension,
and his promise to come again.</TextBlock>
<TextBlock optional>And we earnestly desire your fatherly goodness mercifully to
accept this, our sacrifice of praise and thanksgiving; asking you
to grant that, by the merits and death of your Son Jesus Christ,
and through faith in his Blood, we and your whole Church may
obtain forgiveness of our sins, and all other benefits of his passion.</TextBlock>
<TextBlock>And here we offer and present to you, O Lord, ourselves, our
souls and bodies, to be a reasonable, holy, and living sacrifice. We
humbly pray that all who partake of this Holy Communion may
worthily receive the most precious Body and Blood of your Son
Jesus Christ, be filled with your grace and heavenly benediction,
and be made one body with him, that he may dwell in us, and
we in him.</TextBlock>
<TextBlock optional>And although we are unworthy, because of our many sins, to
offer you any sacrifice, yet we ask you to accept this duty and
service we owe, not weighing our merits, but pardoning our
offenses, through Jesus Christ our Lord.</TextBlock>
<TextBlock>By him, and with him, and in him, in the unity of the Holy
Spirit, all honor and glory is yours, Almighty Father, now and
for ever. **Amen.**</TextBlock>

