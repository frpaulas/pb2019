<p>
	The software you are using (DPB) is a <i>version</i> of the 2019 Book of Common Prayer. It endeavors
	to reproduce the contents of the printed version (PB). The contents are the same; the presentations
	are not. The differences are minor. This is due to a handful of factors:
</p>
<ul>
	<li>The difference in widths of different devices</li>
	<li>Differences in available fonts</li>
	<li>The DPB has no page breaks</li>
	<li>Cost of software development and maintainence</li>
</ul>
<p>
	This DPB is not the canonical version. When questions about the content or presentation arise,
	refer to PB 2019.
</p>
<p>As in all human endeavors, inerrancy is not guaranteed. Refer to the PB.</p>
<p>
	When possible, Scripture content comes from the English Standard Version (ESV). When content from
	the ESV is not available (no access to internet, Apocryphal texts) The World English Bible (WEB)
	is used. The WEB is built into the app and always available. The WEB is used because of copyright
	restrictions and cost.
</p>
