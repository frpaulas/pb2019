<SectionTitle>23. for the harvest of lands and waters</SectionTitle>
<TextBlock>
	O gracious Father, you open your hand and fill all living things with plenteousness: Bless the
	lands and waters, and multiply the harvests of the world; send forth your breath, and renew the
	face of the earth; show your loving-kindness, that our land may yield its increase; and save us
	from selfish use of what you provide, that the poor and needy may give thanks to your Name;
	through Jesus Christ our Lord. **Amen.**
