<SectionTitle>44. in times of social conflict or distress</SectionTitle>
<TextBlock>
	Increase, O God, the spirit of neighborliness among us, that in peril we may uphold one another,
	in suffering tend to one another, and in homelessness, loneliness, or exile befriend one another.
	Grant us brave and enduring hearts that we may strengthen one another, until the disciplines and
	testing of these days are ended, and you again give peace in our time; through Jesus Christ our
	Lord. **Amen.**
