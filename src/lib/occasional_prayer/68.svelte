<SectionTitle>68. for a child, or for a birthday</SectionTitle>
<TextBlock>
	Watch over your child N., O Lord, as his days increase; bless him and guide him, and keep him
	unspotted from the world. Strengthen him when he stands; comfort him when discouraged or
	sorrowful; raise him up if he falls; and in his heart may your peace which passes understanding
	abide all the days of his life; through Jesus Christ our Lord. **Amen.**
