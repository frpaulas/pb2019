<SectionTitle>34. for the sovereign</SectionTitle>
<TextBlock>
	Almighty and everlasting God, we are taught by your holy Word that the hearts of kings are in your
	rule, and that you direct them according to your wisdom: We humbly ask you so to govern the heart
	of N. your servant, our Queen and Governor, that in all her thoughts, words, and deeds, she may
	ever seek your honour and glory, and act to preserve your people committed to her charge in
	prosperity, peace, and godliness; Grant this, O merciful Father, for the sake of your dear Son
	Jesus Christ our Lord. **Amen.**
