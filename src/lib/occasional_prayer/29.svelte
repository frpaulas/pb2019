<SectionTitle>29. for courts of justice</SectionTitle>
<TextBlock>
	Almighty God, you sit on your throne giving righteous judgment: We humbly ask you to bless all
	courts of justice and all magistrates in this land; give them a spirit of wisdom and
	understanding, that fearing no power but yours alone, they may discern the truth and impartially
	administer the law; through him who shall come to be our Judge, your Son our Savior Jesus Christ.
	**Amen.**
