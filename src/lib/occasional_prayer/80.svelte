<SectionTitle>80. for trustfulness in times of worry and anxiety</SectionTitle>
<TextBlock>
	Most loving Father, you will us to give thanks for all things, to dread nothing but the loss of
	you, and to cast all our care on the One who cares for us. Preserve us from faithless fears and
	worldly anxieties, and grant that no clouds of this mortal life may hide from us the light of that
	love which is immortal, and which you have manifested unto us in your Son, Jesus Christ our Lord.
	**Amen.**
