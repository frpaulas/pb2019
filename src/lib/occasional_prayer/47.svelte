<SectionTitle>47. for the unemployed</SectionTitle>
<TextBlock>
	Heavenly Father, we remember before you those who suffer want and anxiety from lack of work. Guide
	the people of this land so to use our public and private wealth that all may find suitable and
	fulfilling employment, and receive a just reward for their labor; through Jesus Christ our Lord.
	**Amen.**
