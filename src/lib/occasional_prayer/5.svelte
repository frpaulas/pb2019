<SectionTitle>5. for the spirit of prayer</SectionTitle>
<TextBlock>
	O Almighty God, you pour out on all who desire it the spirit of grace and of supplication: Deliver
	us, when we draw near to you, from coldness of heart and wanderings of mind, that with steadfast
	thoughts and kindled affections we may worship you in spirit and in truth; through Jesus Christ
	our Lord. **Amen.**
