<SectionTitle>61. for the recovery of a sick person</SectionTitle>
<TextBlock>
	Almighty and immortal God, giver of life and health: We implore your mercy for your servant N.,
	that by your blessing upon him and upon those who minister to him with your healing gifts, he may
	be restored to health of body and mind, according to your gracious will, and may give thanks to
	you in your holy Church; through Jesus Christ our Lord. **Amen.**
