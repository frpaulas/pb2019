<SectionTitle>118. for the mission of the church</SectionTitle>
<TextBlock>
	Almighty God, you sent your Son Jesus Christ to reconcile the world to yourself: We praise and
	bless you for those whom you have sent in the power of the Spirit to preach the Gospel to all
	nations. We thank you that in all parts of the earth a community of love has been gathered
	together by their prayers and labors, and that in every place your servants call upon your Name;
	for the kingdom and the power and the glory are yours, for ever and ever. **Amen.**
