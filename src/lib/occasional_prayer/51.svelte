<SectionTitle>51. for those who inform public opinion</SectionTitle>
<TextBlock>
	Almighty God, your truth endures from age to age: Direct in our time, we pray, those who speak
	where many listen and write what many read; that they may speak your truth to make the heart of
	this people wise, its mind discerning, and its will righteous; to the honor of Jesus Christ our
	Lord. **Amen.**
