<SectionTitle>13. for vocations to ordained ministry</SectionTitle>
<TextBlock>
	Lord Jesus, you are the Good Shepherd who cares for his flock: We ask you to bestow upon your
	Church the gifts of the Holy Spirit in abundance, and to raise up from among us faithful and able
	persons called to the ministries of Deacon, Priest, and Bishop. Inspire them to spend and be spent
	for the sake of the Gospel, and make them holy and loving servants and shepherds of the flock for
	whom you shed your most precious blood. Grant this for the sake of your love. **Amen.**
