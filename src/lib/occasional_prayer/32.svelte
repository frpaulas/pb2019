<SectionTitle>32. for those in the armed forces</SectionTitle>
<TextBlock>
	Almighty God, we commend to your gracious care and keeping all the men and women of our armed
	forces at home and abroad. Defend them day by day with your heavenly grace; strengthen them in
	their trials and temptations; give them courage to face the perils which beset them; and grant
	them a sense of your abiding presence wherever they may be; through Jesus Christ our Lord.
	**Amen.**
