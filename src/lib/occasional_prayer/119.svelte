<SectionTitle>119. for the harvest</SectionTitle>
<TextBlock>
	Most gracious God, by whose knowledge the depths are broken up and the clouds drop down the dew:
	We yield you hearty thanks and praise for the return of seed-time and harvest, for the increase of
	the ground and the gathering in of its fruits, and for all the other blessings your merciful
	providence has bestowed upon this nation and people. And, we pray, give us a just sense of these
	great mercies, that we may walk before you in humility, holiness, and obedience all our days;
	through Jesus Christ our Lord, to whom, with you and the Holy Spirit, be all glory and honor,
	world without end. **Amen.**
