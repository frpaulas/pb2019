<SectionTitle>65. for families</SectionTitle>
<TextBlock>
	Almighty God, our heavenly Father, you set the solitary in families: We commend to your continual
	care the homes in which your people dwell. Put far from them every root of bitterness, the desire
	of vainglory, and the pride of life. Fill them with faith, virtue, knowledge, temperance,
	patience, and true godliness. Knit together in constant affection those who, in holy matrimony,
	have been made one flesh; turn the hearts of parents to their children, and the hearts of children
	to their parents; and so enkindle fervent charity among us all, that we may evermore be joined to
	one another with bonds of loving-kindness; through Jesus Christ our Lord. **Amen.**
