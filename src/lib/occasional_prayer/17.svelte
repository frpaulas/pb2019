<SectionTitle>17. for the mission of the church</SectionTitle>
<TextBlock>
	O God, our heavenly Father, you manifested your love by sending your only-begotten Son into the
	world, that all might live through him: Pour out your Spirit on your Church, that we may fulfill
	his command to preach the Gospel to all people. Send forth laborers into your harvest; defend them
	in all dangers and temptations; and hasten the time when the fullness of the Gentiles shall be
	gathered in, and faithful Israel shall be saved; through your Son Jesus Christ our Lord. **Amen.**
