<SectionTitle>_William Laud_</SectionTitle>
<TextBlock>
	Gracious Father, we pray for your holy Catholic Church. Fill it with all truth, in all truth with
	all peace. Where it is corrupt, purify it; where it is in error, direct it; where in anything it
	is amiss, reform it. Where it is right, strengthen it; where it is in want, provide for it; where
	it is divided, reunite it; for the sake of Jesus Christ your Son our Savior. **Amen.**
