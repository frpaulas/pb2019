<SectionTitle>39. for our nation</SectionTitle>
<TextBlock>
	Almighty God, who hast given us this good land for our heritage: We humbly beseech thee that we
	may always prove ourselves a people mindful of thy favor and glad to do thy will. Bless our land
	with honorable industry, sound learning, and pure conduct. Save us from violence, discord, and
	confusion; from pride and arrogance, and from every evil way. Defend our liberties, and fashion
	into one united people the multitudes brought hither out of many kindreds and tongues. Endue with
	the spirit of wisdom those to whom, in thy Name, we entrust the authority of government, that
	there may be justice and peace at home, and that, through obedience to thy law, we may show forth
	thy praise among the nations of the earth. In the time of prosperity, fill our hearts with
	thankfulness, and in the day of trouble, suffer not our trust in thee to fail; all of which we ask
	through Jesus Christ our Lord. **Amen.**
