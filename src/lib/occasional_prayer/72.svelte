<TextBlock>
	O God, the light of the minds that know you, the life of the souls that love you, and the strength
	of the wills that serve you: Help us so to know you that we may truly love you, and so to love you
	that we may fully serve you, whom to serve is perfect freedom; through Jesus Christ our Lord.
