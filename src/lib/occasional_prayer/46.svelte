<SectionTitle>46. for commerce and industry</SectionTitle>
<TextBlock>
	O Lord Jesus Christ, in your earthly life you shared our toil and hallowed our labor: Guide those
	who maintain the commerce and industries of this land, and give to all who labor pride in their
	work, a just reward, and joy both in supplying need and in serving you; who with the Father and
	the Holy Spirit live and reign, one God, world without end. **Amen.**
