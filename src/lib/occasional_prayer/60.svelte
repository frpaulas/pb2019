<SectionTitle>60. for prisoners</SectionTitle>
<TextBlock>
	O God, you forgive when we deserve punishment, and in your wrath you remember mercy: We humbly ask
	you, of your goodness, to comfort all prisoners [and especially those who are condemned to die].
	Give them a right understanding of themselves, and of your promises, that trusting wholly in your
	mercy, they may not place their confidence anywhere but in you. Relieve the distressed; deliver
	the innocent; bring the guilty to repentance; and as you alone bring light out of darkness, and
	good out of evil, grant that by the power of your Holy Spirit they may be set free from the chains
	of sin, and brought to newness of life; through Jesus Christ our Lord. **Amen.**
