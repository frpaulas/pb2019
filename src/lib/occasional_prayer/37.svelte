<SectionTitle>37. for the president and all in civil authority</SectionTitle>
<TextBlock>
	O Lord our Governor, whose glory fills all the world: We commend this Nation to your merciful
	care, that we may be guided by your providence, and dwell secure in your peace. Grant to the
	President of this Nation, the Governor of this State [or Commonwealth], and to all in authority,
	wisdom and strength to know and to do your will. Fill them with the love of truth and
	righteousness, and make them continually mindful of their calling to serve this people in reverent
	obedience to you; through Jesus Christ our Lord, who lives and reigns with you and the Holy
	Spirit, one God, world without end. **Amen.**
