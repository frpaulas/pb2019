<SectionTitle>3. for the unity of the church</SectionTitle>
<TextBlock>
	Lord Jesus Christ, you said to your apostles, “Peace I give to you; my own peace I leave with
	you”: Regard not our sins, but the faith of your Church, and give to us the peace and unity of
	that heavenly city, where with the Father and the Holy Spirit you live and reign, now and for
	ever. **Amen.**
