<SectionTitle>25. in time of scarcity and famine</SectionTitle>
<SectionTitle>__</SectionTitle>
<TextBlock>
	O God, our heavenly Father, whose blessed Son has taught us to seek our daily bread from you:
	Behold the affliction of your people, and send us swift aid in our time of need. Increase the
	fruits of the earth by your heavenly benediction; and grant that we, receiving your gifts with
	thankful hearts, may use them to your glory, for the relief of those in need, and for our own
	health; through Jesus Christ our Lord. **Amen.**
