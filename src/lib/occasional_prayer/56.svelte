<SectionTitle>56. for the elderly</SectionTitle>
<TextBlock>
	Look with mercy, O God our Father, on all whose increasing years bring them weakness, distress, or
	isolation [especially ______]. Provide for them homes of dignity and peace; give them
	understanding helpers, and the willingness to accept help; and as their strength diminishes,
	increase their faith and their assurance of your love; through Jesus Christ our Lord. **Amen.**
