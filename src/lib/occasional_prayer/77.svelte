<SectionTitle>77. for guidance</SectionTitle>
<TextBlock>
	O God, by whom the meek are guided in judgment, and light rises up in darkness for the godly:
	Grant us, in all our doubts and uncertainties, the grace to ask what you would have us do, that
	the Spirit of wisdom may save us from all false choices; that in your light we may see light, and
	in your straight path we may not stumble; through Jesus Christ our Lord. **Amen.**
