<SectionTitle>54. for the absent</SectionTitle>
<TextBlock>
	O God, whose fatherly care reaches to the ends of the earth: We ask you graciously to behold and
	bless those we love who are now absent from us [especially _____]. Defend them from all dangers of
	soul and body, and grant that both they and we, drawing nearer to you, may be bound together by
	your love, in the communion of your Holy Spirit and in the fellowship of your saints; through
	Jesus Christ our Lord. **Amen.**
