<SectionTitle>79. for mercy</SectionTitle>
<TextBlock>
	Almighty God, you have not dealt with us according to our sins, nor rewarded us according to our
	iniquities; grant that we, who for our evil deeds deserve to be punished, by the might of your
	grace may mercifully be relieved; through our Lord and Savior Jesus Christ, who lives and reigns
	with you and the Holy Spirit, one God, for ever and ever. **Amen.**
