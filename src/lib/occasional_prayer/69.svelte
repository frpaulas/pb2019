<SectionTitle>69. for a marriage or anniversary</SectionTitle>
<TextBlock>
	O God, you have so consecrated the covenant of marriage that in it is represented the spiritual
	unity between Christ and his Church: Send your blessing upon these your servants [as they begin
	another year], that they may so love, honor, and cherish each other in faithfulness and patience,
	in wisdom and true godliness, that their home may be a haven of blessing and peace; through Jesus
	Christ our Lord, who lives and reigns with you and the Holy Spirit, one God, now and for ever.
	**Amen.**
