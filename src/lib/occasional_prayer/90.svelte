<SectionTitle>_Benedict of Nursia_</SectionTitle>
<TextBlock>
	Gracious and holy Father, please give me intellect to understand you, reason to discern you,
	diligence to seek you, wisdom to find you, a spirit to know you, a heart to meditate upon you,
	ears to hear you, eyes to see you, a tongue to proclaim you, a way of life pleasing to you,
	patience to wait for you, and perseverance to look for you. Grant me a perfect end, your holy
	presence, a blessed resurrection, and life everlasting. **Amen.**
