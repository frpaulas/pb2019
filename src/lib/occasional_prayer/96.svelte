<SectionTitle>96. for union with christ</SectionTitle>
<SectionTitle>_Anima Christi_</SectionTitle>
<TextBlock>
	Soul of Christ, sanctify me. Body of Christ, save me. Blood of Christ, inebriate me. Water from
	the side of Christ, wash me. Passion of Christ, strengthen me. O good Jesus, hear me. Within thy
	wounds hide me. Permit me not to be separated from thee. From the wicked foe defend me. In the
	hour of my death call me, and bid me come to thee, that with thy saints I may praise thee for ever
	and ever. **Amen.**
