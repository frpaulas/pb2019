<SectionTitle>100. for the answering of prayer</SectionTitle>
<TextBlock>
	Almighty God, you have promised to hear the petitions of those who ask in the Name of your Son:
	Mercifully incline your ear to us as we make our prayers and supplications to you; and grant that
	what we ask faithfully, according to your will, we may obtain effectually, for the relief of our
	necessities and the setting forth of your glory; through Jesus Christ our Lord. **Amen.**
