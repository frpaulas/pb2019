<SectionTitle>110. for joy at the end of life</SectionTitle>
<SectionTitle>_Miles Coverdale_</SectionTitle>
<TextBlock>
	Lord Jesus, be mindful of your promise. Think of us, your servants, and when we shall depart,
	speak to our spirits these loving words: “Today you shall be with me in joy.” O Lord Jesus Christ,
	remember us, your servants who trust in you, when our tongues cannot speak, when the sight of our
	eyes fails, and when our ears are stopped. Let our spirits always rejoice in you and be joyful
	about our salvation, which you, through your death, have purchased for us. **Amen.**
