<SectionTitle>114. for all faithful departed</SectionTitle>
<TextBlock>
	Almighty God, with whom the souls of the faithful who have departed this life are in joy and
	felicity: We praise and magnify your holy Name for all your servants who have finished their
	course in your faith and fear; and we most humbly pray that, at the day of resurrection, we and
	all who are members of the mystical body of your Son may be set on his right hand, and hear his
	most joyful voice: “Come, you who are blessed by my Father, inherit the kingdom prepared for you
	from the foundation of the world.” Grant this, O merciful Father, for the sake of Jesus Christ,
	our only Mediator and Advocate. **Amen.**
