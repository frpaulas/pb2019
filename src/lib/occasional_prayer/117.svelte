<TextBlock>
	Accept, O Lord, our thanks and praise for all that you have done for us. We thank you for the
	splendor of the whole creation, for the beauty of this world, for the wonder of life, and for the
	mystery of love. We thank you for the blessing of family and friends, and for the loving care
	which surrounds us on every side. We thank you for setting us at tasks that demand our best
	efforts, and for leading us to accomplishments that satisfy and delight us. We thank you also for
	those disappointments and failures that lead us to acknowledge our dependence on you alone. Above
	all, we thank you for your Son Jesus Christ; for the truth of his Word and the example of his
	life; for his steadfast obedience, by which he overcame temptation; for his dying, through which
	he conquered death; and for his rising to life again, in which we are raised to the life of your
	kingdom. Grant us the gift of your Spirit, that we may know Christ and make him known; and through
	him, at all times and in all places, may give thanks to you in all things. **Amen.**
