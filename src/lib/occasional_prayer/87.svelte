<SectionTitle>87. for participation in the peace of god</SectionTitle>
<SectionTitle>_In the Tradition of Francis of Assisi_</SectionTitle>
<TextBlock>
	Lord, make me an instrument of your peace: where there is hatred, let me sow love; where there is
	injury, pardon; where there is discord, union; where there is error, truth; where there is doubt,
	faith; where there is despair, hope; where there is darkness, light; where there is sadness, joy.
	O divine Master, grant that I may seek not so much to be consoled as to console, to be understood
	as to understand, to be loved as to love. For it is in giving that we receive, it is in pardoning
	that we are pardoned, and it is in dying that we are born to eternal life. **Amen.**
