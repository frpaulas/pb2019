<SectionTitle>59. for the discouraged and downcast</SectionTitle>
<TextBlock>
	O God, almighty and merciful, you heal the broken-hearted, and turn the sadness of the sorrowful
	to joy, Let your fatherly goodness be upon all whom you have made. Remember in pity all those who
	are this day destitute, homeless, elderly, infirm, or forgotten. Bless the multitude of your poor.
	Lift up those who are cast down. Mightily befriend innocent sufferers, and sanctify to them the
	endurance of their wrongs. Cheer with hope all who are discouraged and downcast, and by your
	heavenly grace preserve from falling those whose poverty tempts them to sin. Though they be
	troubled on every side, suffer them not to be distressed; though they are perplexed, save them
	from despair. Grant this, O Lord, for the love of him who for our sakes became poor, your Son our
	Savior Jesus Christ. **Amen.**
