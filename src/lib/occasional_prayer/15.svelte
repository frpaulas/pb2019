<SectionTitle>15. for monastic orders and vocations</SectionTitle>
<TextBlock>
	O Lord Jesus Christ, you became poor for our sake that we might be made rich through your poverty:
	Guide and sanctify, we pray, those whom you call to follow you in poverty, chastity, and
	obedience; that by their prayer and service they may enrich your Church, and by their life and
	worship may glorify your Name; for you live and reign with the Father and the Holy Spirit, one
	God, now and for ever. **Amen.**
