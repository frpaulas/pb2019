<SectionTitle>22. for stewardship of creation</SectionTitle>
<TextBlock>
	O merciful Creator, your loving hand is open wide to satisfy the needs of every living creature:
	Make us always thankful for your loving providence, and give us grace to honor you with all that
	you have entrusted to us; that we, remembering the account we must one day give, may be faithful
	stewards of your good gifts; through Jesus Christ our Lord, who with you and the Holy Spirit lives
	and reigns, one God, for ever and ever. **Amen.**
