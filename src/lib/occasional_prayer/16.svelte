<SectionTitle>16. for the mission of the church</SectionTitle>
<TextBlock>
	O God, you have made of one blood all the peoples of the earth, and sent your blessed Son to
	preach peace to those who are far off and to those who are near: Grant that people everywhere may
	seek after you and find you; bring the nations into your fold; pour out your Spirit upon all
	flesh; and hasten the coming of your kingdom; through Jesus Christ our Lord. **Amen.**
