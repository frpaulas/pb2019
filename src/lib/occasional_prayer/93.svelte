<SectionTitle>93. a covenant prayer</SectionTitle>
<SectionTitle>_John Wesley_</SectionTitle>
<TextBlock>
	I am no longer my own, but thine. Put me to what thou wilt, rank me with whom thou wilt. Put me to
	doing, put me to suffering. Let me be employed by thee or laid aside for thee, exalted for thee or
	brought low for thee. Let me be full, let me be empty. Let me have all things, let me have
	nothing. I freely and heartily yield all things to thy pleasure and disposal. And now, O glorious
	and blessed God, Father, Son, and Holy Spirit, thou art mine, and I am thine. So be it. And the
	covenant which I have made on earth, let it be ratified in heaven. **Amen.**
