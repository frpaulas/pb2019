<SectionTitle>81. for help to bear bereavement</SectionTitle>
<TextBlock>
	Heavenly Father, help us to entrust our loved ones to your care. Though sorrow darkens our lives,
	help us to look up to you, remembering the cloud of witnesses by which we are surrounded. And
	grant that we on earth, rejoicing ever in your presence, may share with them the rest and peace
	which your presence gives; through Jesus Christ our Lord. **Amen.**
