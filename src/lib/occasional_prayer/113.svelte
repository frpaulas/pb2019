<SectionTitle>113. the communion of saints</SectionTitle>
<TextBlock>
	O eternal Lord God, you hold all souls in life: Shed forth upon your whole Church in Paradise and
	on earth the bright beams of your light and heavenly comfort; and grant that we, following the
	good example of those who have loved and served you here and are now at rest, may enter with them
	into the fullness of your unending joy; through Jesus Christ our Lord. **Amen.**
