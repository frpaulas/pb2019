<SectionTitle>7. for a provincial or diocesan convention or synod</SectionTitle>
<TextBlock>
	Almighty and everlasting God, by your Holy Spirit you presided in the council of the blessed
	Apostles, and you promised, through your Son Jesus Christ, to be with your Church to the end of
	the world: Be with the council of your Church assembled [here] in your Name and presence. Save us
	from all error, ignorance, prejudice, and pride; and of your great mercy direct, sanctify, and
	govern us in our work, by the mighty power of the Holy Spirit; that the order and discipline of
	your Church may be maintained, and that the Gospel of Christ may be truly preached, truly
	received, and truly followed in all places, breaking down the kingdom of sin, Satan, and death;
	till all your scattered sheep, being gathered into one fold, become partakers of everlasting life;
	through the merits and death of Jesus Christ our Savior. **Amen.**
