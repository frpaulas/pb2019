<SectionTitle>109. for watchfulness</SectionTitle>
<SectionTitle>_Lancelot Andrewes_</SectionTitle>
<TextBlock>
	Thou, who with thine own mouth hast told us that at midnight the bridegroom shall come: Grant that
	the cry, “The bridegroom cometh!” may sound evermore in our ears, that so we be never unprepared
	to meet him, or forgetful of the souls for whom he died, for whom we watch and pray. And save us,
	O Lord. **Amen.**
