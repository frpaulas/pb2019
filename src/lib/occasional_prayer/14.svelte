<SectionTitle>14. for church musicians and artists</SectionTitle>
<TextBlock>
	O God, whom saints and angels delight to worship in heaven: Be ever present with your servants on
	earth who seek through art and music to perfect the praises of your people. Grant them even now
	true glimpses of your beauty, and make them worthy at length to behold it unveiled for evermore;
	through Jesus Christ our Lord. **Amen.**
