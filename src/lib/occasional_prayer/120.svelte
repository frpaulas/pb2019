<SectionTitle>120. for the beauty of the earth</SectionTitle>
<TextBlock>
	We give you thanks, most gracious God, for the beauty of earth and sky and sea; for the richness
	of mountains, plains, and rivers; for the wonder of your creatures, large and small; and for all
	the loveliness that surrounds us. We praise you for these good gifts, and pray that we may
	safeguard them for our posterity. Grant that we may continue to grow in our grateful enjoyment of
	your abundant creation, to the honor and glory of your Name, now and for ever. **Amen.**
