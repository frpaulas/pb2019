<SectionTitle>11. for the local congregation</SectionTitle>
<TextBlock>
	O God the Holy Spirit, Sanctifier of the faithful: Sanctify this Congregation by your abiding
	presence. Bless those who minister in holy things. Enlighten the minds of your people more and
	more with the light of the everlasting Gospel. Bring erring souls to the knowledge of our Savior
	Jesus Christ; and those who are walking in the way of life, keep steadfast to the end. Give
	patience to the sick and afflicted, and renew them in body and soul. Guard those who are strong
	and prosperous from forgetting you. Increase in us your many gifts of grace, and make us all
	fruitful in good works. This we ask, O blessed Spirit, whom with the Father and the Son we worship
	and glorify, one God, world without end. **Amen.**
