<SectionTitle>75. for holy thought</SectionTitle>
<TextBlock>
	O God, without whose beauty and goodness our souls are unfed, without whose truth our reason
	withers: Consecrate our lives to your will, giving us such purity of heart, such depth of faith,
	and such steadfastness of purpose, that in time we may come to think your own thoughts after you;
	through Jesus Christ our Savior. **Amen.**
