<SectionTitle>40. for all sorts and conditions of men</SectionTitle>
<TextBlock>
	O God, the creator and preserver of all mankind, we humbly beseech thee for all sorts and
	conditions of men; that thou wouldest be pleased to make thy ways known unto them, thy saving
	health unto all nations. More especially we pray for thy holy Church universal, that it may be so
	guided and governed by thy good Spirit, that all who profess and call themselves Christians may be
	led into the way of truth, and hold the faith in unity of spirit, in the bond of peace, and in
	righteousness of life. Finally, we commend to thy fatherly goodness all those who are in any ways
	afflicted or distressed, in mind, body, or estate, [especially those for whom our prayers are
	desired]; that it may please thee to comfort and relieve them according to their several
	necessities, giving them patience under their sufferings, and a happy issue out of all their
	afflictions. And this we beg for Jesus Christâ€™s sake. **Amen.**
