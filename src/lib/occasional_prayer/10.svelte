<SectionTitle>10. for the selection of a bishop or other minister</SectionTitle>
<TextBlock>
	Almighty God, giver of every good gift: Look graciously on your Church, and so guide the minds of
	those who shall choose a Bishop for this Diocese that we may receive a faithful pastor who will
	preach the Gospel, care for your people, equip us for ministry, and lead us forth in fulfillment
	of the Great Commission; through Jesus Christ our Lord. **Amen.**
