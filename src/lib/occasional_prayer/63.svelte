<SectionTitle>63. for those in bondage to addiction</SectionTitle>
<TextBlock>
	O blessed Lord, you ministered to all who came to you: Look with compassion upon those who through
	addiction have lost their health and freedom. Restore to them the assurance of your unfailing
	mercy; remove from them the fears that beset them; strengthen them in the work of their recovery;
	and to those who minister to them, give patient understanding and persevering love; through Jesus
	Christ our Lord. **Amen.**
