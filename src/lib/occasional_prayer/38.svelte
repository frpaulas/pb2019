<SectionTitle>8. for congress or a state legislature</SectionTitle>
<TextBlock>
	O God, the fountain of wisdom, whose will is good and gracious, and whose law is truth: So guide
	and bless our Senators and Representatives assembled in Congress [or the Legislature of this
	State, Commonwealth, etc.], that they may enact laws pleasing in your sight, to the glory of your
	Name and the welfare of this people; through Jesus Christ our Lord. **Amen.**
