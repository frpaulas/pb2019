<SectionTitle>4. for the unity of all christian people</SectionTitle>
<TextBlock>
	O God the Father of our Lord Jesus Christ, our only Savior, the Prince of Peace: Give us grace to
	take to heart the grave dangers we are in through our many divisions. Deliver your Church from all
	enmity and prejudice, and everything that hinders us from godly union. As there is one Body and
	one Spirit, one hope of our calling, one Lord, one Faith, one Baptism, one God and Father of us
	all, so make us all to be of one heart and of one mind, united in one holy bond of truth and
	peace, of faith and love, that with one voice we may give you praise; through Jesus Christ our
	Lord, who lives and reigns with you and the Holy Spirit, one God in everlasting glory. **Amen.**
