<SectionTitle>94. for a virtuous heart</SectionTitle>
<SectionTitle>_Thomas Aquinas_</SectionTitle>
<TextBlock>
	Give me, O Lord, a steadfast heart, which no unworthy thought can drag down; an unconquered heart,
	which no tribulation can wear out; an upright heart, which no unworthy purpose can tempt aside.
	Bestow upon me understanding to know you, diligence to seek you, wisdom to find you, and
	faithfulness that finally may embrace you. **Amen.**
