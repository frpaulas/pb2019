<SectionTitle>106. for spiritual communion</SectionTitle>
<TextBlock>
	Dear Jesus, I believe that you are truly present in the Holy Sacrament. I love you above all
	things, and I desire to possess you within my soul. And since I cannot now receive you
	sacramentally, I beseech you to come spiritually into my heart. I unite myself to you, together
	with all your faithful people [gathered around every altar of your Church], and I embrace you with
	all the affections of my soul. Never permit me to be separated from you. **Amen.**
