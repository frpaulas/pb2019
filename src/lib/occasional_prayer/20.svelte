<SectionTitle>20. for a spirit of evangelism</SectionTitle>
<TextBlock>
	Almighty God our Savior, you desire that none should perish, and you have taught us through your
	Son that there is great joy in heaven over every sinner who repents: Grant that our hearts may
	ache for a lost and broken world. May your Holy Spirit work through our words, deeds, and prayers,
	that the lost may be found and the dead made alive, and that all your redeemed may rejoice around
	your throne; through Jesus Christ our Lord. **Amen.**
