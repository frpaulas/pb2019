<SectionTitle>24. for rain</SectionTitle>
<TextBlock>
	O God, our heavenly Father, by your Son Jesus Christ you have promised to those who seek your
	kingdom and its righteousness all things necessary to sustain their life: Send us, we pray, in
	this time of need, such moderate rain and showers, that we may receive the fruits of the earth, to
	our comfort and to your honor; through Jesus Christ our Lord. **Amen.**
