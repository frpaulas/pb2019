<SectionTitle>28. for the peace of the world</SectionTitle>
<TextBlock>
	Almighty God, from whom all thoughts of truth and peace proceed: Kindle, we pray, in the hearts of
	all people the true love of peace, and guide with your pure and peaceable wisdom those who take
	counsel for the nations of the earth; that in tranquility your kingdom may go forward, till the
	earth is filled with the knowledge of your love; through Jesus Christ our Lord. **Amen.**
