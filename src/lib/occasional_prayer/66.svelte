<SectionTitle>66. for the care of children</SectionTitle>
<TextBlock>
	Almighty God, heavenly Father, you have blessed us with the joy and care of children: Give us calm
	strength and patient wisdom so to train them, that they may love all that is true, and pure, and
	lovely, and of good report, following the example of their Savior Jesus Christ. **Amen.**
