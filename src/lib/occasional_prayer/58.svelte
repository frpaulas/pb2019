<SectionTitle>58. for a person in trouble or bereavement</SectionTitle>
<TextBlock>
	O merciful Father, you have taught us in your holy Word that you do not willingly afflict or
	grieve the children of men: Look with pity on the sorrows of your servant N. Remember him, O Lord,
	in mercy; nourish his soul with patience; comfort him with a sense of your goodness; lift up your
	countenance upon him; and give him peace; through Jesus Christ our Lord. **Amen.**
