<SectionTitle>64. for the unrepentant</SectionTitle>
<TextBlock>
	Merciful God, you desire not the death of sinners, but rather that they should turn to you and
	live; and through your only Son you have revealed yourself as the God who pardons iniquity. Have
	mercy on the unrepentant and those who do not believe [especially _____]. Awaken in them, by your
	Word and Holy Spirit, a deep sense of their sinfulness and peril. Take from them all ignorance,
	hardness of heart, and contempt of your Word. Grant them to know and feel that there is no other
	Name under heaven given among men by which they must be saved, but only the Name of the Lord Jesus
	Christ. And so bring them home and number them among your children, that they may be yours for
	ever; through Jesus Christ our Lord, who lives and reigns with you and the Holy Spirit, one God,
	world without end. **Amen.**
