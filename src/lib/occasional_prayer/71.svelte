<SectionTitle>71. for christ to be formed in us</SectionTitle>
<TextBlock>
	Lord Jesus, Master Carpenter of Nazareth, on the Cross through wood and nails you wrought our full
	salvation: Wield well your tools in this, your workshop, that we who come to you rough-hewn may be
	fashioned into a truer beauty by your hand; who with the Father and the Holy Spirit live and
	reign, one God, world without end. **Amen.**
