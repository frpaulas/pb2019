<SectionTitle>_Week of the Sunday from July 24 to July 30_</SectionTitle>
<TextBlock>
	Almighty and everlasting God, you are always more ready to hear than we to pray, and to give more
	than we either desire or deserve: Pour down upon us the abundance of your mercy, forgiving us
	those things of which our conscience is afraid, and giving us those good things for which we are
	not worthy to ask, except through the merits and mediation of Jesus Christ our Savior; who lives
	and reigns with you and the Holy Spirit, one God, for ever and ever. **Amen.**</TextBlock
